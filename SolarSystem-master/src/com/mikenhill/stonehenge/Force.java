package com.mikenhill.stonehenge;

public class Force {
	private double xForce = 0;
	private double yForce = 0;
	public double getxForce() {
		return xForce;
	}
	public void setxForce(double xForce) {
		this.xForce = xForce;
	}
	public double getyForce() {
		return yForce;
	}
	public void setyForce(double yForce) {
		this.yForce = yForce;
	}
	
	
}
